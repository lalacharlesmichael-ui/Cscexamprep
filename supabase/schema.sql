create extension if not exists citext;

create table if not exists public.profiles (
  id bigint generated by default as identity primary key,
  auth_id uuid not null unique references auth.users(id) on delete cascade,
  full_name text not null,
  username citext not null unique check (username::text ~ '^[a-z0-9._-]+$'),
  role text not null default 'user' check (role in ('admin', 'user')),
  created_at timestamptz not null default now()
);

create table if not exists public.registration_requests (
  id bigint generated by default as identity primary key,
  full_name text not null,
  username citext not null unique check (username::text ~ '^[a-z0-9._-]+$'),
  status text not null default 'pending'
    check (status in ('pending', 'approved', 'declined', 'completed')),
  approval_code text unique check (approval_code is null or approval_code ~ '^[0-9]{6}$'),
  requested_at timestamptz not null default now(),
  reviewed_at timestamptz,
  reviewed_by bigint references public.profiles(id) on delete set null
);

create table if not exists public.exam_types (
  id bigint generated by default as identity primary key,
  exam_name text not null unique
);

create table if not exists public.exam_areas (
  id bigint generated by default as identity primary key,
  exam_type_id bigint not null references public.exam_types(id) on delete cascade,
  area_name text not null,
  unique (exam_type_id, area_name)
);

create table if not exists public.exam_sub_areas (
  id bigint generated by default as identity primary key,
  area_id bigint not null references public.exam_areas(id) on delete cascade,
  sub_area_name text not null,
  unique (area_id, sub_area_name)
);

create table if not exists public.questions (
  id bigint generated by default as identity primary key,
  exam_type_id bigint not null references public.exam_types(id),
  area_id bigint not null references public.exam_areas(id),
  sub_area_id bigint not null references public.exam_sub_areas(id),
  question_text text not null,
  option_a text not null,
  option_b text not null,
  option_c text not null,
  option_d text not null,
  correct_answer text not null check (correct_answer in ('A', 'B', 'C', 'D')),
  explanation text not null default '',
  created_at timestamptz not null default now()
);

create table if not exists public.quiz_attempts (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.profiles(id) on delete cascade,
  exam_type_id bigint not null references public.exam_types(id),
  area_id bigint references public.exam_areas(id),
  sub_area_id bigint references public.exam_sub_areas(id),
  quiz_mode text not null check (quiz_mode in ('overall', 'area', 'subArea')),
  score integer not null check (score >= 0),
  total_questions integer not null check (total_questions > 0),
  time_limit_per_question integer not null check (time_limit_per_question > 0),
  total_time_seconds integer not null check (total_time_seconds >= 0),
  date_taken timestamptz not null default now()
);

create table if not exists public.user_answers (
  id bigint generated by default as identity primary key,
  attempt_id bigint not null references public.quiz_attempts(id) on delete cascade,
  question_id bigint not null references public.questions(id),
  selected_answer text check (selected_answer in ('A', 'B', 'C', 'D')),
  time_spent_seconds integer not null check (time_spent_seconds >= 0),
  is_timeout boolean not null default false,
  is_correct boolean not null,
  unique (attempt_id, question_id)
);

create or replace function public.current_profile_id()
returns bigint
language sql
stable
security definer
set search_path = public
as $$
  select id from public.profiles where auth_id = auth.uid()
$$;

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1 from public.profiles where auth_id = auth.uid() and role = 'admin'
  )
$$;

drop function if exists public.request_registration(text, text);
create function public.request_registration(
  p_full_name text,
  p_username text
)
returns text
language plpgsql
security definer
set search_path = public
as $$
declare
  v_full_name text := trim(coalesce(p_full_name, ''));
  v_username text := lower(trim(coalesce(p_username, '')));
  v_status text;
  v_existing_code text;
  v_code text;
begin
  perform pg_advisory_xact_lock(hashtext('cscquiz_registration_requests')::bigint);

  if v_full_name = '' then
    raise exception 'Full name is required.';
  end if;
  if v_username = '' or v_username !~ '^[a-z0-9._-]+$' then
    raise exception 'Enter a valid username.';
  end if;
  if exists (select 1 from public.profiles where username = v_username) then
    raise exception 'That username is already registered.';
  end if;

  select status, approval_code into v_status, v_existing_code
  from public.registration_requests
  where username = v_username;

  -- The first account bootstraps the system administrator. Every later
  -- account requires approval from that administrator.
  if not exists (select 1 from public.profiles) then
    if v_status = 'approved' and v_existing_code is not null then
      return v_existing_code;
    end if;
    if exists (
      select 1 from public.registration_requests where status = 'approved'
    ) then
      raise exception 'Initial administrator setup is already in progress.';
    end if;
    perform pg_advisory_xact_lock(hashtext('cscquiz_registration_codes')::bigint);
    loop
      v_code := lpad(floor(random() * 1000000)::integer::text, 6, '0');
      exit when not exists (
        select 1 from public.registration_requests where approval_code = v_code
      );
    end loop;
    insert into public.registration_requests (
      full_name, username, status, approval_code, requested_at, reviewed_at, reviewed_by
    )
    values (v_full_name, v_username, 'approved', v_code, now(), now(), null)
    on conflict (username) do update set
      full_name = excluded.full_name,
      status = 'approved',
      approval_code = excluded.approval_code,
      requested_at = now(),
      reviewed_at = now(),
      reviewed_by = null;
    return v_code;
  end if;

  if v_status = 'pending' then
    raise exception 'That username already has a pending approval request.';
  end if;
  if v_status = 'approved' then
    raise exception 'That request is already approved. Ask the administrator for the registration code.';
  end if;

  insert into public.registration_requests (
    full_name, username, status, approval_code, requested_at, reviewed_at, reviewed_by
  )
  values (v_full_name, v_username, 'pending', null, now(), null, null)
  on conflict (username) do update set
    full_name = excluded.full_name,
    status = 'pending',
    approval_code = null,
    requested_at = now(),
    reviewed_at = null,
    reviewed_by = null;
  return null;
end;
$$;

create or replace function public.approve_registration(p_request_id bigint)
returns text
language plpgsql
security definer
set search_path = public
as $$
declare
  v_code text;
  v_status text;
begin
  if not public.is_admin() then
    raise exception 'Administrator access is required.';
  end if;

  select status, approval_code into v_status, v_code
  from public.registration_requests
  where id = p_request_id
  for update;

  if v_status is null then
    raise exception 'Registration request not found.';
  end if;
  if v_status = 'approved' and v_code is not null then
    return v_code;
  end if;
  if v_status <> 'pending' then
    raise exception 'Only pending registration requests can be approved.';
  end if;

  perform pg_advisory_xact_lock(hashtext('cscquiz_registration_codes')::bigint);
  loop
    v_code := lpad(floor(random() * 1000000)::integer::text, 6, '0');
    exit when not exists (
      select 1 from public.registration_requests where approval_code = v_code
    );
  end loop;

  update public.registration_requests
  set
    status = 'approved',
    approval_code = v_code,
    reviewed_at = now(),
    reviewed_by = public.current_profile_id()
  where id = p_request_id;

  return v_code;
end;
$$;

create or replace function public.decline_registration(p_request_id bigint)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.is_admin() then
    raise exception 'Administrator access is required.';
  end if;

  update public.registration_requests
  set
    status = 'declined',
    approval_code = null,
    reviewed_at = now(),
    reviewed_by = public.current_profile_id()
  where id = p_request_id
    and status in ('pending', 'approved');

  if not found then
    raise exception 'Pending or approved registration request not found.';
  end if;
end;
$$;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_request_id bigint;
  v_full_name text;
  v_username text := lower(coalesce(nullif(new.raw_user_meta_data ->> 'username', ''), split_part(new.email, '@', 1)));
  v_approval_code text := trim(coalesce(new.raw_user_meta_data ->> 'approval_code', ''));
begin
  select id, full_name into v_request_id, v_full_name
  from public.registration_requests
  where username = v_username
    and status = 'approved'
    and approval_code = v_approval_code
  for update;

  if v_request_id is null then
    raise exception 'A valid administrator approval code is required.';
  end if;

  insert into public.profiles (auth_id, full_name, username, role)
  values (
    new.id,
    v_full_name,
    v_username,
    case when not exists (select 1 from public.profiles) then 'admin' else 'user' end
  );

  update public.registration_requests
  set status = 'completed', approval_code = null
  where id = v_request_id;

  return new;
end;
$$;

