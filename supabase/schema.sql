create or replace function public.submit_quiz(
  p_exam_type_id bigint,
  p_area_id bigint,
  p_sub_area_id bigint,
  p_quiz_mode text,
  p_time_limit_per_question integer,
  p_answers jsonb
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  v_attempt_id bigint;
  v_user_id bigint := public.current_profile_id();
  v_score integer;
  v_total_questions integer;
  v_total_time integer;
begin
  if v_user_id is null then
    raise exception 'A signed-in profile is required.';
  end if;
  if coalesce(jsonb_typeof(p_answers), 'null') <> 'array' or jsonb_array_length(p_answers) = 0 then
    raise exception 'At least one answer is required.';
  end if;

  with submitted as (
    select
      (item ->> 'question_id')::bigint as question_id,
      case when item ->> 'selected_answer' in ('A', 'B', 'C', 'D')
        then item ->> 'selected_answer' else null end as selected_answer,
      greatest(0, least(p_time_limit_per_question, coalesce((item ->> 'time_spent_seconds')::integer, 0))) as time_spent_seconds
    from jsonb_array_elements(p_answers) as answers(item)
  )
  select
    count(*) filter (where submitted.selected_answer = questions.correct_answer),
    count(*),
    coalesce(sum(submitted.time_spent_seconds), 0)
  into v_score, v_total_questions, v_total_time
  from submitted
  join public.questions on questions.id = submitted.question_id;

  if v_total_questions <> jsonb_array_length(p_answers) then
    raise exception 'One or more submitted questions are invalid.';
  end if;

  insert into public.quiz_attempts (
    user_id, exam_type_id, area_id, sub_area_id, quiz_mode, score,
    total_questions, time_limit_per_question, total_time_seconds
  )
  values (
    v_user_id, p_exam_type_id, p_area_id, p_sub_area_id, p_quiz_mode, v_score,
    v_total_questions, p_time_limit_per_question, v_total_time
  )
  returning id into v_attempt_id;

  insert into public.user_answers (
    attempt_id, question_id, selected_answer, time_spent_seconds, is_timeout, is_correct
  )
  select
    v_attempt_id,
    (item ->> 'question_id')::bigint,
    case when item ->> 'selected_answer' in ('A', 'B', 'C', 'D')
      then item ->> 'selected_answer' else null end,
    greatest(0, least(p_time_limit_per_question, coalesce((item ->> 'time_spent_seconds')::integer, 0))),
    coalesce((item ->> 'is_timeout')::boolean, false),
    coalesce((item ->> 'selected_answer') = questions.correct_answer, false)
  from jsonb_array_elements(p_answers) as answers(item)
  join public.questions on questions.id = (item ->> 'question_id')::bigint;

  return v_attempt_id;
end;
$$;

